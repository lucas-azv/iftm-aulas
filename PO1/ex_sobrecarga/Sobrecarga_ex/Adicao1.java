public class Adicao1 {
    public static int addInt(int a, int b) {
        return a + b;
    }

    public static float addFloat(float c, float d) {
        return c + d;
    }

    public static String exibeInt(int result) {
        return "Resultado da adição: " + result;
    }

    public static String exibeFloat(float result) {
        return "Resultado da adição: " + result;
    }
}