public class Adicao2 {
    public static int add(int a, int b) {
        return a + b;
    }

    public static float add(float c, float d) {
        return c + d;
    }

    public static String exibe(int result) {
        return "Resultado da adição: " + result;
    }

    public static String exibe(float result) {
        return "Resultado da adição: " + result;
    }
}